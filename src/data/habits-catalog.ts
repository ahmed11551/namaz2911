export const searchHabits = (query: string): Habit[] => {
  const lowerQuery = query.toLowerCase();
  return HABITS_CATALOG.filter(habit =>
    habit.title.toLowerCase().includes(lowerQuery) ||
    habit.description.toLowerCase().includes(lowerQuery) ||
    habit.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
  );
};

